// Helper function to get products by category
export const getProductsByCategory = (categoryId) => {
  return sampleProducts.filter((p) => p.categoryId === categoryId && p.status === 'active');
};

// Helper function to get featured products
export const getFeaturedProducts = () => {
  return sampleProducts.filter((p) => p.isFeatured && p.status === 'active');
};

// Helper function to get parent categories
export const getParentCategories = () => {
  return sampleCategories.filter((c) => c.parentId === null && c.status === 'active');
};

// Helper function to get sub-categories
export const getSubCategories = (parentId) => {
  return sampleCategories.filter((c) => c.parentId === parentId && c.status === 'active');
};

// Get category by slug
export const getCategoryBySlug = (slug) => {
  return sampleCategories.find((c) => c.slug === slug);
};

// Get product by slug
export const getProductBySlug = (slug) => {
  return sampleProducts.find((p) => p.slug === slug);
};

// Search products
export const searchProducts = (query) => {
  const lowerQuery = query.toLowerCase();
  return sampleProducts.filter(
    (p) =>
      p.status === 'active' &&
      (p.name.toLowerCase().includes(lowerQuery) ||
        p.shortDescription.toLowerCase().includes(lowerQuery) ||
        p.tags.some((tag) => tag.includes(lowerQuery)) ||
        p.flavors.some((f) => f.toLowerCase().includes(lowerQuery)))
  );
};
